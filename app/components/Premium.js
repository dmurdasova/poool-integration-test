export default ({ children }) => {
  const accessRef = useRef();
  const paywallRef = useRef();
  const navigate = useNavigate();

  const { lib: audit, config: auditConfig } = useAudit();
  const { config: baseConfig } = useAccess();
  const { login, premium, connected } = useAuth();

  const [config, setConfig] = useState(null);

  useEffect(() => {
    setTimeout(() => {
      setConfig({});
    }, 1000 * 3);
  }, []);

  useEffect(() => {
    if (config) {
      accessRef.current?.recreate();
    }
  }, [config]);

  const init = () => {
    setConfig({
      ...baseConfig,
      ...config,
      user_is_premium: premium || false,
    });

    if (connected && premium) {
      accessRef.current?.destroy();
    }
  };

  const onLogin = async () => {
    init();
    audit?.config({
      ...auditConfig,
      user_is_premium: premium || false,
    });
    audit?.sendEvent("page-view", "premium");
  };

  const onSubscribeClick = (e) => {
    e.originalEvent?.preventDefault();
  };

  const onLoginClick = async (e) => {
    e.originalEvent?.preventDefault();
    await login();
    onLogin();
  };

  let paragraphCounter = 0;
  const childrenCount = children.length;

  const articleBody = values.map((value) => {
    // Start at 1 since the ad configs use one-based array indexes
    paragraphCounter += 1;

    if (children.length && paragraphCounter < childrenCount - 1) {
      const child = children[paragraphCounter - 1];

      return [<div>{value}</div>, child ?? null];
    } else {
      return <div>{value}</div>;
    }
  });

  if (!config) {
    return <div>Loading...</div>;
  }

  return (
    <div className="page premium">
      <div className="container">
        <Header onLogin={onLogin} />
        <h1>Premium post</h1>
        {!premium ? (
          <>
            <RestrictedContent ref={paywallRef}>
              <div className="articleBody">{articleBody}</div>
            </RestrictedContent>
            <Paywall
              ref={accessRef}
              contentRef={paywallRef}
              events={{ onSubscribeClick, onLoginClick }}
              config={config}
            />
          </>
        ) : (
          <div className="articleBody">{articleBody}</div>
        )}
      </div>
      <Pixel type="page-view" data={{ type: "premium" }} />
    </div>
  );
};
